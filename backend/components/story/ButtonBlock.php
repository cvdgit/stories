<?php


namespace backend\components\story;


class ButtonBlock extends AbstractBlock
{

    /** @var string */
    protected $title;

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle(string $title): void
    {
        $this->title = $title;
    }

}